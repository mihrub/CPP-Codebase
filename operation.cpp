#include<iostream>
int main(){

    double students = 6 - 5 + 4 * 3 / 2;

    students = students + 1;
    std:: cout << students <<'\n' ;

    students +=1;
    std:: cout << students <<'\n' ;

    students++;
    std:: cout << students <<'\n' ;

    students = students - 2;
    std:: cout << students <<'\n' ;

    students -=5;
    std:: cout << students <<'\n' ;

    students --;
    std:: cout << students <<'\n' ;

    return 0;
}